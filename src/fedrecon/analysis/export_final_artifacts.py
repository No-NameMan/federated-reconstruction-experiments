def export_final_artifacts(
    *,
    project_root: str | Path = ".",
    output_dir: str | Path = "results/final",
    overwrite: bool = True,
) -> list[tuple[Path, Path, bool]]:
    project_root = Path(project_root).resolve()
    output_dir = project_root / output_dir

    copied: list[tuple[Path, Path, bool]] = []

    for spec in DEFAULT_ARTIFACTS:
        src = project_root / spec.source
        dst = output_dir / spec.target

        exists = src.exists()
        copied.append((src, dst, exists))

        if not exists:
            continue

        dst.parent.mkdir(parents=True, exist_ok=True)

        if dst.exists() and not overwrite:
            continue

        shutil.copy2(src, dst)

    readme = output_dir / "README.md"
    readme.parent.mkdir(parents=True, exist_ok=True)
    readme.write_text(
        "# Final results package\n\n"
        "This directory contains selected tables and figures intended for the course paper and presentation.\n\n"
        "Generated by `scripts/export_final_artifacts.py`.\n",
        encoding="utf-8",
    )

    return copied
